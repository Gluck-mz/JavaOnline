public class Rectangle {
    private int sideA;
    private int sideB;
    private int sideC;
    private int sideD;

    Rectangle(int sideA, int sideB, int sideC, int sideD){
        this.sideA = 10;
        this.sideB = 4;
        this.sideC = 10;
        this.sideD = 4;
    }

    public int getSideA() {
        return sideA;
    }

    public int getSideB() {
        return sideB;
    }

    public int getSideC() {
        return sideC;
    }

    public int getSideD() {
        return sideD;
    }
}
